solve :: (Ord a, RealFloat a) => [(a, a)] -> a
solve points = let hullPoints = buildHullPoints points
                   sideLengths = zipWith distanceBetweenPoints hullPoints (tail hullPoints)
               in sum sideLengths

buildHullPoints :: (Ord a, RealFloat a) => [(a, a)] -> [(a,a)]
buildHullPoints points = next [leftMost]
  where leftMost = minimumBy (compare `on` fst) points
        next list = let point = last list
                        restOfPoints = filter (`notElem` [last list]) points
                        vector = if length list >= 2 then vectorFromPoints (secondLast list) point else (0, 1)
                        nextPoint = minimumAnglePoint point vector restOfPoints
                    in if nextPoint == leftMost then list ++ [nextPoint] else next (list ++ [nextPoint])

minimumAnglePoint :: (Ord a, RealFloat a) => (a,a) -> (a,a) -> [(a, a)] -> (a,a)
minimumAnglePoint point vector points = minimumBy (compare `on` angleBetween vector . vectorFromPoints point) points
