vectorFromPoints :: RealFloat a => (a,a) -> (a,a) -> (a,a)
vectorFromPoints (x,y) (x',y') = (x'-x,y'-y)

dotProduct :: RealFloat a => (a,a) -> (a,a) -> a
dotProduct (x,y) (x',y') = x * x' + y * y'

vectorLength :: RealFloat a => (a, a) -> a
vectorLength (x,y) = sqrt (x ** 2 + y ** 2)

distanceBetweenPoints :: RealFloat a => (a,a) -> (a,a) -> a
distanceBetweenPoints pt1 pt2 = vectorLength $ vectorFromPoints pt1 pt2

angleBetween :: RealFloat a => (a,a) -> (a,a) -> a
-- angleBetween (x,y) (x',y') = abs (atan2 y' x' - atan2 y x)
angleBetween (x,y) (x',y') = acos (dotProduct (x,y) (x',y') / (vectorLength (x,y) * vectorLength (x',y')))

secondLast :: [a] -> a
secondLast = last . init

